import 'package:isi_steel_sales_mobile/core/utils/typedefs.dart';
import 'package:isi_steel_sales_mobile/features/order/domain/entities/product.dart';
import 'package:isi_steel_sales_mobile/features/order/domain/entities/product_pricing.dart';
import 'package:isi_steel_sales_mobile/features/order/domain/entities/product_status.dart';
import 'package:isi_steel_sales_mobile/features/order/domain/entities/promotion_type.dart';

/// Hand-written `fromJson`/`fromRow`/`toXRow` mapping (mirrors `UserModel`'s
/// style) rather than leaning on `json_serializable` — the mock remote
/// payload and the joined-sqlite-row shape are different enough (nested vs.
/// flat-aliased) that generated code wouldn't buy much here.
class ProductModel extends Product {
  const ProductModel({
    required super.id,
    required super.familyId,
    required super.familyName,
    required super.code,
    required super.sku,
    required super.materialCode,
    required super.barcode,
    required super.name,
    required super.description,
    required super.categoryId,
    required super.subCategory,
    required super.brand,
    required super.grade,
    required super.material,
    required super.size,
    required super.diameter,
    required super.thickness,
    required super.length,
    required super.width,
    required super.height,
    required super.weight,
    required super.unit,
    required super.warehouseCode,
    required super.territory,
    required super.businessUnit,
    required super.imageUrl,
    required super.isMto,
    required super.status,
    required super.updatedAt,
    required super.pricing,
    required super.stockQuantity,
    required super.reservedQuantity,
    required super.minStock,
    required super.maxStock,
    this.deleted = false,
  });

  final bool deleted;

  factory ProductModel.fromJson(DataMap json) => ProductModel(
        id: json['id'] as String,
        familyId: json['familyId'] as String,
        familyName: json['familyName'] as String,
        code: json['code'] as String,
        sku: json['sku'] as String,
        materialCode: json['materialCode'] as String,
        barcode: json['barcode'] as String,
        name: json['name'] as String,
        description: json['description'] as String,
        categoryId: json['categoryId'] as String,
        subCategory: json['subCategory'] as String,
        brand: json['brand'] as String,
        grade: json['grade'] as String,
        material: json['material'] as String,
        size: json['size'] as String,
        diameter: (json['diameter'] as num).toDouble(),
        thickness: (json['thickness'] as num).toDouble(),
        length: (json['length'] as num).toDouble(),
        width: (json['width'] as num).toDouble(),
        height: (json['height'] as num).toDouble(),
        weight: (json['weight'] as num).toDouble(),
        unit: json['unit'] as String,
        warehouseCode: json['warehouseCode'] as String,
        territory: json['territory'] as String,
        businessUnit: json['businessUnit'] as String,
        imageUrl: json['imageUrl'] as String,
        isMto: json['isMto'] as bool,
        status: ProductStatus.values.byName(json['status'] as String),
        updatedAt: DateTime.parse(json['updatedAt'] as String),
        deleted: json['deleted'] as bool? ?? false,
        pricing: _pricingFromJson(json['pricing'] as DataMap),
        stockQuantity: (json['stockQuantity'] as num).toDouble(),
        reservedQuantity: (json['reservedQuantity'] as num).toDouble(),
        minStock: (json['minStock'] as num).toDouble(),
        maxStock: (json['maxStock'] as num).toDouble(),
      );

  static ProductPricing _pricingFromJson(DataMap json) => ProductPricing(
        costPrice: (json['costPrice'] as num).toDouble(),
        standardPrice: (json['standardPrice'] as num).toDouble(),
        wholesalePrice: (json['wholesalePrice'] as num).toDouble(),
        dealerPrice: (json['dealerPrice'] as num).toDouble(),
        vipPrice: (json['vipPrice'] as num).toDouble(),
        creditPrice: (json['creditPrice'] as num).toDouble(),
        cashPrice: (json['cashPrice'] as num).toDouble(),
        currency: json['currency'] as String,
        promotionPrice: (json['promotionPrice'] as num?)?.toDouble(),
        promotionType: json['promotionType'] == null
            ? null
            : PromotionType.values.byName(json['promotionType'] as String),
        promotionLabel: json['promotionLabel'] as String?,
      );

  factory ProductModel.fromRow(DataMap row) => ProductModel(
        id: row['id'] as String,
        familyId: row['family_id'] as String,
        familyName: row['family_name'] as String,
        code: row['code'] as String,
        sku: row['sku'] as String,
        materialCode: row['material_code'] as String,
        barcode: row['barcode'] as String,
        name: row['name'] as String,
        description: row['description'] as String,
        categoryId: row['category_id'] as String,
        subCategory: row['sub_category'] as String,
        brand: row['brand'] as String,
        grade: row['grade'] as String,
        material: row['material'] as String,
        size: row['size'] as String,
        diameter: (row['diameter'] as num).toDouble(),
        thickness: (row['thickness'] as num).toDouble(),
        length: (row['length'] as num).toDouble(),
        width: (row['width'] as num).toDouble(),
        height: (row['height'] as num).toDouble(),
        weight: (row['weight'] as num).toDouble(),
        unit: row['unit'] as String,
        warehouseCode: row['warehouse_code'] as String,
        territory: row['territory'] as String,
        businessUnit: row['business_unit'] as String,
        imageUrl: row['image_url'] as String,
        isMto: (row['is_mto'] as int) == 1,
        status: ProductStatus.values.byName(row['status'] as String? ?? 'active'),
        updatedAt: DateTime.parse(row['updated_at'] as String),
        deleted: (row['deleted'] as int? ?? 0) == 1,
        pricing: ProductPricing(
          costPrice: (row['cost_price'] as num?)?.toDouble() ?? 0,
          standardPrice: (row['standard_price'] as num?)?.toDouble() ?? 0,
          wholesalePrice: (row['wholesale_price'] as num?)?.toDouble() ?? 0,
          dealerPrice: (row['dealer_price'] as num?)?.toDouble() ?? 0,
          vipPrice: (row['vip_price'] as num?)?.toDouble() ?? 0,
          creditPrice: (row['credit_price'] as num?)?.toDouble() ?? 0,
          cashPrice: (row['cash_price'] as num?)?.toDouble() ?? 0,
          currency: row['currency'] as String? ?? 'USD',
          promotionPrice: (row['promotion_price'] as num?)?.toDouble(),
          promotionType: row['promotion_type'] == null
              ? null
              : PromotionType.values.byName(row['promotion_type'] as String),
          promotionLabel: row['promotion_label'] as String?,
        ),
        stockQuantity: (row['stock_quantity'] as num?)?.toDouble() ?? 0,
        reservedQuantity: (row['reserved_quantity'] as num?)?.toDouble() ?? 0,
        minStock: (row['min_stock'] as num?)?.toDouble() ?? 0,
        maxStock: (row['max_stock'] as num?)?.toDouble() ?? 0,
      );

  DataMap toProductRow() => {
        'id': id,
        'family_id': familyId,
        'family_name': familyName,
        'code': code,
        'sku': sku,
        'material_code': materialCode,
        'barcode': barcode,
        'name': name,
        'description': description,
        'category_id': categoryId,
        'sub_category': subCategory,
        'brand': brand,
        'grade': grade,
        'material': material,
        'size': size,
        'diameter': diameter,
        'thickness': thickness,
        'length': length,
        'width': width,
        'height': height,
        'weight': weight,
        'unit': unit,
        'warehouse_code': warehouseCode,
        'territory': territory,
        'business_unit': businessUnit,
        'image_url': imageUrl,
        'is_mto': isMto ? 1 : 0,
        'status': status.name,
        'updated_at': updatedAt.toIso8601String(),
        'deleted': deleted ? 1 : 0,
        'min_stock': minStock,
        'max_stock': maxStock,
      };

  DataMap toPriceRow() => {
        'product_id': id,
        'cost_price': pricing.costPrice,
        'standard_price': pricing.standardPrice,
        'wholesale_price': pricing.wholesalePrice,
        'dealer_price': pricing.dealerPrice,
        'vip_price': pricing.vipPrice,
        'credit_price': pricing.creditPrice,
        'cash_price': pricing.cashPrice,
        'promotion_price': pricing.promotionPrice,
        'promotion_type': pricing.promotionType?.name,
        'promotion_label': pricing.promotionLabel,
        'currency': pricing.currency,
        'updated_at': updatedAt.toIso8601String(),
      };

  DataMap toStockRow() => {
        'product_id': id,
        'warehouse_code': warehouseCode,
        'quantity': stockQuantity,
        'reserved': reservedQuantity,
        'updated_at': updatedAt.toIso8601String(),
      };

  DataMap toFtsRow() => {
        'product_id': id,
        'code': code,
        'name': name,
        'barcode': barcode,
        'sku': sku,
        'brand': brand,
      };
}
